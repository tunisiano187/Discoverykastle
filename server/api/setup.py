# Routes
# ──────────────────────────────────────────────────────────────────────────────

@router.get("/setup", response_class=HTMLResponse)
async def setup_page(request: Request) -> HTMLResponse:
    """Render the setup wizard. Redirect to / if already configured."""
    if not setup_needed():
        return RedirectResponse("/", status_code=302)  # type: ignore[return-value]

    current = _load_env_values()
    return _TEMPLATES.TemplateResponse(
        "setup.html",
        {
            "request": request,
            "v": current,
            "ai_backends": _available_backends(),
        },
    )


@router.post("/setup/save", response_class=HTMLResponse)
async def setup_save(request: Request) -> HTMLResponse:
    """Receive form data, write .env, reload settings, redirect."""
    form = await request.form()
    values: dict[str, str] = {}

    for key, val in form.items():
        if isinstance(val, str):
            values[key.upper()] = val.strip()

    # Auto-generate keys if submitted empty
    if not values.get("DKASTLE_SECRET_KEY") or values["DKASTLE_SECRET_KEY"] == _DEFAULT_SECRET:
        values["DKASTLE_SECRET_KEY"] = secrets.token_hex(32)
    if not values.get("DKASTLE_VAULT_KEY") or values["DKASTLE_VAULT_KEY"] == "changeme-base64-32-bytes":
        values["DKASTLE_VAULT_KEY"] = base64.b64encode(secrets.token_bytes(32)).decode()

    _write_env(values)
    _reload_settings()

    return _TEMPLATES.TemplateResponse(
        "setup.html",
        {
            "request": request,
            "v": values,
            "ai_backends": _available_backends(),
            "saved": True,
        },
    )


@router.get("/setup/generate-key")
async def generate_key(kind: str = "hex") -> dict[str, str]:
    """API endpoint called by the setup form JS to generate secure keys."""
    if kind == "base64":
        return {"key": base64.b64encode(secrets.token_bytes(32)).decode()}
    return {"key": secrets.token_hex(32)}


@router.get("/setup/generate-vapid")
async def generate_vapid() -> dict[str, str]:
    """Generate a new VAPID key pair for Web Push. Returns private + public keys."""
    from server.services.webpush import generate_vapid_keys
    return generate_vapid_keys()


