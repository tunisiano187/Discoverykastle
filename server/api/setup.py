# ──────────────────────────────────────────────────────────────────────────────

def _reload_settings() -> None:
    """Force pydantic-settings to re-read .env after it has been written."""
    try:
        import importlib
        import server.config as cfg_module
        importlib.reload(cfg_module)
    except Exception:
        pass


def _available_backends() -> list[dict[str, str]]:
    """Return registered backend names and labels for the setup UI."""
    try:
        from server.modules.builtin.ai.backends import REGISTRY
        return [
            {"name": name, "label": cls.label, "requires_key": str(cls.requires_key).lower()}
            for name, cls in REGISTRY.items()
        ]
    except Exception:
        return [
            {"name": "ollama",    "label": "Ollama (local)",    "requires_key": "false"},
            {"name": "anthropic", "label": "Anthropic (cloud)", "requires_key": "true"},
        ]
